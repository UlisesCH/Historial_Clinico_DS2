    public void Guardar(){
        //OBJETO PARA ENTERACTUAR CON LA CONEXION
        Conexion conec = new Conexion();
        //CREA REALIZA LA CONEXION Y CREA LA TABLA SI NO HAY
        conec.CrearTablas();

        //CICLO PARA LLENAR LA TABLA CON LOS VALORES DEL ARREGLO
        for(int PosC = 0; PosC < listaInClRecibo.size(); PosC++){

            //OBJETO PARA ENTERACTUAR CON EL CRUD
            CRUD_InContable Cr = new CRUD_InContable();
            //SE MANDA LOS VALORES AL INSERTAR
            Cr.Insertar(listaInClRecibo.get(PosC).getNombProducto(), listaInClRecibo.get(PosC).getCantidad()
                        , listaInClRecibo.get(PosC).getNombProveedor(), listaInClRecibo.get(PosC).getPrecioProducto()
                        , listaInClRecibo.get(PosC).getTipoCuenta(),listaInClRecibo.get(PosC).getFecha());
        }
        
        JOptionPane.showMessageDialog(null, "DATOS GUARDADOS");
        
        TxtNombProducto.setText("");
        TxtCantidad.setText("");
        TxtNombProveedor.setText("");
        TxtPrecioProducto.setText("");
        TxtTotalExamen.setText("Total");
        TxtFecha.setText("Fecha");
        
        listaInClRecibo.clear();
        Llenar();
        
    }
    
    public void Llenar(){
        Double Total = 0.0;
        
        //SE LIMPIA LA TABLA
        model.setRowCount(0);
        
        //CICLO PARA LLENAR LA TABLA CON LOS VALORES DEL ARREGLO
        for(int PosC = 0; PosC < listaInClRecibo.size(); PosC++){
           
            model.addRow(new Object[]{listaInClRecibo.get(PosC).getNombProducto()
                        ,listaInClRecibo.get(PosC).getCantidad(),listaInClRecibo.get(PosC).getPrecioProducto()});
            
            TxtFecha.setText(listaInClRecibo.get(PosC).getFecha());
            
            Total = Total+listaInClRecibo.get(PosC).getPrecioProducto();
        }
        
        
        TxtTotalExamen.setText(""+Total);
        
    }
   
