        Double PrecioProducto = Double.parseDouble(TxtPrecioProducto.getText());
        String TipoCuenta = (String) CombxCuenta.getSelectedItem();
        //SE ALMACENA LOS VALOR DE LA FECHA
        String Fecha = dia+ "/" +mes+ "/" +anio;

        //OBJETO DEL CONSTRUCTOR
        InConRecibo InConRecibo = new InConRecibo(NombProducto, Cantidad, NombProveedor, PrecioProducto, TipoCuenta, Fecha);

        //SE AGREGA EL CONSTRUCTOR AL ARREGLO
        listaInClRecibo.add(InConRecibo);

        Llenar();

